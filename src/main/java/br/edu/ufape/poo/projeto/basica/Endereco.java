package br.edu.ufape.poo.projeto.basica;

public class Endereco {
	private String cep;
	private String rua;
	private String cidade;
	private String estado;
	private int numero;
	
	
	public Endereco(String cep, String rua, String cidade, String estado, int numero) {
		super();
		this.cep = cep;
		this.rua = rua;
		this.cidade = cidade;
		this.estado = estado;
		this.numero = numero;
	}
	
	
}
